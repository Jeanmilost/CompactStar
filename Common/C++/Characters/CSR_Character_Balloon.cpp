//---------------------------------------------------------------------------
bool CSR_Character_Balloon::Build()
{
    // the scene should always exist
    if (!m_pScene)
        return false;

    // build the model
    CSR_Model* pModel = BuildModel(m_HasNormal, m_HasTexCoords, m_Color);

    // succeeded?
    if (!pModel)
        return false;

    // set the model key
    m_pKey = pModel;

    // set the texture key
    m_pTextureKey = &pModel->m_pMesh->m_Skin.m_Texture;

    // link the model with the scene
    const CSR_SceneItem* pSceneItem = csrSceneAddModel(m_pScene, pModel, 0, m_Collisions ? 1 : 0);
    csrSceneAddModelMatrix(m_pScene, pSceneItem->m_pModel, &m_Matrix);

    const float ropeHeight = 0.05f;
    const float ropeStep   = ropeHeight / 10.0f;
          float curStep    = 0.0f;

    m_Anchors.resize(10);
    m_Segments.resize(9);

    // iterate through rope anchor and segments to create
    for (std::size_t i = 0; i < 10; ++i)
    {
        // calculate the next anchor
        m_Anchors[i].m_X = 0.0f;
        m_Anchors[i].m_Y = curStep;
        m_Anchors[i].m_Z = 0.0f;

        // update the step to create the next anchor
        curStep -= ropeStep;

        // segments can be created only since the second anchor
        if (!i)
            continue;

        // create a new line segment
        std::auto_ptr<CSR_Line> pLine(new CSR_Line());
        csrLineInit(pLine.get());
        pLine->m_Start.m_X = m_Anchors[i - 1].m_X;
        pLine->m_Start.m_Y = m_Anchors[i - 1].m_Y;
        pLine->m_Start.m_Z = m_Anchors[i - 1].m_Z;
        pLine->m_End.m_X   = m_Anchors[i].m_X;
        pLine->m_End.m_Y   = m_Anchors[i].m_Y;
        pLine->m_End.m_Z   = m_Anchors[i].m_Z;
        pLine->m_Width     = 1.0f;

        // add it to the segment list
        m_Segments[i] = pLine.get();

        // add it to the scene
        csrSceneAddLine(m_pScene, pLine.get(), 0);
        pLine.release();
    }

    return true;
}
//---------------------------------------------------------------------------
CSR_Model* CSR_Character_Balloon::BuildModel(bool hasNormal, bool hasTexCoords, unsigned color) const
{
    // get the WaveFront model
    const std::string waveFront = GetWaveFront();

    CSR_Buffer buffer;
    buffer.m_pData  = (void*)waveFront.c_str();
    buffer.m_Length = waveFront.length();

    // configure the vertex format
    CSR_VertexFormat vertexFormat;
    vertexFormat.m_HasNormal         = hasNormal    ? 1 : 0;
    vertexFormat.m_HasTexCoords      = hasTexCoords ? 1 : 0;
    vertexFormat.m_HasPerVertexColor = 0;

    CSR_VertexCulling vertexCulling;
    vertexCulling.m_Type = CSR_CT_Front;
    vertexCulling.m_Face = CSR_CF_CW;

    // configure the material
    CSR_Material material;
    material.m_Color       = color;
    material.m_Transparent = 0;
    material.m_Wireframe   = 0;

    return csrWaveFrontCreate(&buffer, &vertexFormat, &vertexCulling, &material, 0, 0, m_fOnDeleteTexture);
}
